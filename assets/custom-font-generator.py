def double_glyph(glyph):
    """Scale a 5x7 glyph to 10x14 (2x in both directions)."""
    doubled = []
    for row in glyph:
        new_row = []
        for pixel in row:
            new_row.extend([pixel, pixel])  # double width
        doubled.append(new_row)
        doubled.append(new_row.copy())  # double height
    return doubled


